#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –ø–µ—Ä–≤–æ–Ω–∞—á–∞–ª—å–Ω–æ–π –Ω–∞—Å—Ç—Ä–æ–π–∫–∏
# –ü–æ–º–æ–≥–∞–µ—Ç –Ω–∞—Å—Ç—Ä–æ–∏—Ç—å IP —Å–µ—Ä–≤–µ—Ä–∞ –∏ –ø—Ä–æ–≤–µ—Ä–∏—Ç—å –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é

set -e

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

# –§—É–Ω–∫—Ü–∏–∏ –¥–ª—è –≤—ã–≤–æ–¥–∞
log() {
    echo -e "${BLUE}[$(date +'%Y-%m-%d %H:%M:%S')]${NC} $1"
}

success() {
    echo -e "${GREEN}‚úÖ $1${NC}"
}

warning() {
    echo -e "${YELLOW}‚ö†Ô∏è  $1${NC}"
}

error() {
    echo -e "${RED}‚ùå $1${NC}"
}

echo "üöÄ –ù–∞—Å—Ç—Ä–æ–π–∫–∞ —Ä–∞–∑–≤–µ—Ä—Ç—ã–≤–∞–Ω–∏—è Telegram-–±–æ—Ç–∞"
echo "=========================================="

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ .env —Ñ–∞–π–ª–∞
if [ ! -f "bot/.env" ]; then
    error "–§–∞–π–ª bot/.env –Ω–µ –Ω–∞–π–¥–µ–Ω!"
    echo "–°–æ–∑–¥–∞–π—Ç–µ —Ñ–∞–π–ª bot/.env —Å –≤–∞—à–∏–º —Ç–æ–∫–µ–Ω–æ–º –±–æ—Ç–∞:"
    echo "BOT_TOKEN=your_bot_token_here"
    exit 1
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ç–æ–∫–µ–Ω –±–æ—Ç–∞
BOT_TOKEN=$(grep "^BOT_TOKEN=" bot/.env | cut -d'=' -f2)
if [ -z "$BOT_TOKEN" ]; then
    error "BOT_TOKEN –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ bot/.env"
    exit 1
fi

success "–¢–æ–∫–µ–Ω –±–æ—Ç–∞ –Ω–∞–π–¥–µ–Ω: ${BOT_TOKEN:0:10}..."

# –ü—Ä–æ–≤–µ—Ä—è–µ–º config.sh
if [ ! -f "deploy/config.sh" ]; then
    error "–§–∞–π–ª deploy/config.sh –Ω–µ –Ω–∞–π–¥–µ–Ω!"
    exit 1
fi

# –ß–∏—Ç–∞–µ–º —Ç–µ–∫—É—â–∏–π IP —Å–µ—Ä–≤–µ—Ä–∞
CURRENT_IP=$(grep "^SERVER_IP=" deploy/config.sh | cut -d'"' -f2)

if [ -z "$CURRENT_IP" ]; then
    echo ""
    log "IP —Å–µ—Ä–≤–µ—Ä–∞ –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω –≤ deploy/config.sh"
    echo ""
    echo "–í–≤–µ–¥–∏—Ç–µ IP –∞–¥—Ä–µ—Å –≤–∞—à–µ–≥–æ —Å–µ—Ä–≤–µ—Ä–∞:"
    read -p "IP —Å–µ—Ä–≤–µ—Ä–∞: " SERVER_IP
    
    if [ -z "$SERVER_IP" ]; then
        error "IP —Å–µ—Ä–≤–µ—Ä–∞ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –ø—É—Å—Ç—ã–º"
        exit 1
    fi
    
    # –û–±–Ω–æ–≤–ª—è–µ–º config.sh
    sed -i.bak "s/^SERVER_IP=\"\"/SERVER_IP=\"$SERVER_IP\"/" deploy/config.sh
    success "IP —Å–µ—Ä–≤–µ—Ä–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω: $SERVER_IP"
else
    echo ""
    log "–¢–µ–∫—É—â–∏–π IP —Å–µ—Ä–≤–µ—Ä–∞: $CURRENT_IP"
    echo ""
    echo "–•–æ—Ç–∏—Ç–µ –∏–∑–º–µ–Ω–∏—Ç—å IP —Å–µ—Ä–≤–µ—Ä–∞? (y/n)"
    read -p "–û—Ç–≤–µ—Ç: " CHANGE_IP
    
    if [ "$CHANGE_IP" = "y" ] || [ "$CHANGE_IP" = "Y" ]; then
        echo ""
        echo "–í–≤–µ–¥–∏—Ç–µ –Ω–æ–≤—ã–π IP –∞–¥—Ä–µ—Å —Å–µ—Ä–≤–µ—Ä–∞:"
        read -p "IP —Å–µ—Ä–≤–µ—Ä–∞: " NEW_IP
        
        if [ -z "$NEW_IP" ]; then
            error "IP —Å–µ—Ä–≤–µ—Ä–∞ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –ø—É—Å—Ç—ã–º"
            exit 1
        fi
        
        # –û–±–Ω–æ–≤–ª—è–µ–º config.sh
        sed -i.bak "s/^SERVER_IP=\"$CURRENT_IP\"/SERVER_IP=\"$NEW_IP\"/" deploy/config.sh
        success "IP —Å–µ—Ä–≤–µ—Ä–∞ –æ–±–Ω–æ–≤–ª–µ–Ω: $NEW_IP"
    fi
fi

# –ó–∞–≥—Ä—É–∂–∞–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–Ω—É—é –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é
source deploy/config.sh

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ —Å–µ—Ä–≤–µ—Ä—É
echo ""
log "–ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ —Å–µ—Ä–≤–µ—Ä—É $SERVER_IP..."

if ssh -o ConnectTimeout=10 -o BatchMode=yes $SERVER_USER@$SERVER_IP exit 2>/dev/null; then
    success "–ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ —Å–µ—Ä–≤–µ—Ä—É —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ"
else
    warning "–ù–µ —É–¥–∞–µ—Ç—Å—è –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ —Å–µ—Ä–≤–µ—Ä—É $SERVER_IP"
    echo ""
    echo "–í–æ–∑–º–æ–∂–Ω—ã–µ –ø—Ä–∏—á–∏–Ω—ã:"
    echo "1. –°–µ—Ä–≤–µ—Ä –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω"
    echo "2. SSH –∫–ª—é—á–∏ –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω—ã"
    echo "3. –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–π IP –∞–¥—Ä–µ—Å"
    echo ""
    echo "–î–ª—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ SSH –∫–ª—é—á–µ–π –≤—ã–ø–æ–ª–Ω–∏—Ç–µ:"
    echo "ssh-keygen -t rsa -b 4096"
    echo "ssh-copy-id $SERVER_USER@$SERVER_IP"
    echo ""
    echo "–ü—Ä–æ–¥–æ–ª–∂–∏—Ç—å –Ω–∞—Å—Ç—Ä–æ–π–∫—É? (y/n)"
    read -p "–û—Ç–≤–µ—Ç: " CONTINUE
    
    if [ "$CONTINUE" != "y" ] && [ "$CONTINUE" != "Y" ]; then
        exit 1
    fi
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø—Ä–∞–≤–∞ –Ω–∞ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ —Å–∫—Ä–∏–ø—Ç–æ–≤
echo ""
log "–ü—Ä–æ–≤–µ—Ä—è–µ–º –ø—Ä–∞–≤–∞ –Ω–∞ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ —Å–∫—Ä–∏–ø—Ç–æ–≤..."
chmod +x deploy/*.sh
success "–ü—Ä–∞–≤–∞ –Ω–∞ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã"

# –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Å–ª–µ–¥—É—é—â—É—é –∫–æ–º–∞–Ω–¥—É
echo ""
echo "=========================================="
echo "üéâ –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞!"
echo "=========================================="
echo ""
echo "–¢–µ–ø–µ—Ä—å –≤—ã –º–æ–∂–µ—Ç–µ —Ä–∞–∑–≤–µ—Ä–Ω—É—Ç—å –±–æ—Ç–∞ –∫–æ–º–∞–Ω–¥–æ–π:"
echo ""
echo "  ./deploy/deploy_smart.sh"
echo ""
echo "–ò–ª–∏ –ø—Ä–æ–≤–µ—Ä–∏—Ç—å —Å—Ç–∞—Ç—É—Å:"
echo ""
echo "  ./deploy/monitor_smart.sh"
echo ""
echo "–ò–ª–∏ —Å–æ–∑–¥–∞—Ç—å —Ä–µ–∑–µ—Ä–≤–Ω—É—é –∫–æ–ø–∏—é:"
echo ""
echo "  ./deploy/backup_smart.sh"
echo ""
echo "üöÄ –£–¥–∞—á–∏ —Å —Ä–∞–∑–≤–µ—Ä—Ç—ã–≤–∞–Ω–∏–µ–º!"
